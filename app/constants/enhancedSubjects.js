// Helper function to get all subjects as a flat array (for backend compatibility)
export const getAllSubjects = () => {
  return Object.keys(ENHANCED_SUBJECTS).map(key => ENHANCED_SUBJECTS[key].name);
};

// Helper function to get specific grade level subjects
export const getGradeLevelSubjects = (subjectKey, gradeLevel) => {
  const subject = ENHANCED_SUBJECTS[subjectKey];
  if (!subject) return [];
  
  const grade = subject.gradelevels.find(g => g.name === gradeLevel);
  return grade ? grade.topics : [];
};

// Helper function to get parent subject from full subject string
export const getParentSubject = (fullSubject) => {
  const parts = fullSubject.split(' - ');
  return parts[0] || fullSubject;
};
